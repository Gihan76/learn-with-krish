
public class Employee {

	private int id;
	private String name;
	private int age;
	private String position;
	
	public Employee(int id,String name,int age,String position) {
		super();
		this.id = id;
		this.name = name;
		this.age = age;
		this.position = position;
	}
	
	@Override
	public String toString() {
		return super.toString();
	}
	
}
